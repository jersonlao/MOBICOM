package com.example.object;

public class Diary {
	
	private int diaryID;
	private String babyName;
	private int babyDOB;
	private String babyPOB;
	private int gender;
	private int diaryColor;
	
	public int getDiaryID() {
		return diaryID;
	}
	public String getBabyName() {
		return babyName;
	}
	public int getBabyDOB() {
		return babyDOB;
	}
	public String getBabyPOB() {
		return babyPOB;
	}
	public int getGender() {
		return gender;
	}
	public int getDiaryColor() {
		return diaryColor;
	}
	public void setDiaryID(int diaryID) {
		this.diaryID = diaryID;
	}
	public void setBabyName(String babyName) {
		this.babyName = babyName;
	}
	public void setBabyDOB(int babyDOB) {
		this.babyDOB = babyDOB;
	}
	public void setBabyPOB(String babyPOB) {
		this.babyPOB = babyPOB;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public void setDiaryColor(int diaryColor) {
		this.diaryColor = diaryColor;
	}
	
}
